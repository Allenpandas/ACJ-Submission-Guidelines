/*
 *	例题6.7输出一个菱形
 *	(题意：先画好一个菱形,实际是5X5的矩阵数组，将数组中的字符串逐个输出)
 *			    *    
 *			  *   *  
 *          *       *
 *			  *   *  	
 *			    *    		
 **/

#include <stdio.h>
int main()
{
	char diamond[][5] = {{' ', ' ', '*'}, {' ', '*', ' ', '*'}, {'*', ' ', ' ', ' ', '*'},{' ', '*', ' ', '*'}, {' ', ' ', '*'}};
	int i,j;
	for(i=0;i<5;i++)
	{
		for(j=0;j<5;j++)
		{
			printf("%c",diamond[i][j]);	//字符串用%c输出
		}
		printf("\n");
	}
	return 0;
}